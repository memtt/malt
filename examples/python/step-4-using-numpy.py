#!/usr/bin/env python3

###########################################################
import numpy

###########################################################
N = 400000

###########################################################
def gen_vector(factor: float):
    a = [None] * N
    for k in range(N):
        a[k] = k * factor
    return numpy.array(a)

###########################################################
def main():
    a = gen_vector(2.5)
    b = gen_vector(5.8)
    c = a + b
    d = numpy.sum(c)
    print(d)

###########################################################
if __name__ == "__main__":
    main()

